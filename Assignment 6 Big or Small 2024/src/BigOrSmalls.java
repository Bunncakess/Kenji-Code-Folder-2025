public class BigOrSmalls {

    public void check( int a, int b ){
        if (a > b){
            System.out.println("A is bigger, B is smaller");
        }
        else if (a == b){
            System.out.println("A and B are equals");
        }
        else{
            System.out.println("B is bigger, A is smaller");
        }
    }
    
}
