public class FtoC {
    public void Fahrenheitcalculator(double num){
        System.out.println("The conversion is: " + (num - 32) * 5/9 );
    }
    public void Celciuscalculator(double num2){
        System.out.println("The conversion is: " + (num2 * 9/5 + 32));
    }
}
