public class Geometry{

    public static void PerimeterCal(double Plenght, double Pwidth){
        System.out.println("The Perimeter is: " + (2 * Plenght + 2 * Pwidth));
    }

    public static void CubeCal(double SAedge){
        System.out.println("The Edge is: " + (6 * (SAedge * SAedge)));
    }

    public static void AreaCal(double AofCradius){
        System.out.println("The Radius is: " + (Math.PI * (AofCradius * AofCradius)));
    }


}